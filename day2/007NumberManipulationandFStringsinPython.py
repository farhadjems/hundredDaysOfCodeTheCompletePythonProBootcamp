
#* round() function rounds number to int by just giving the number
#* takes number of decimals as 2nd argument
print(round(8/3))
print(round(8/3, 3))

#* Module % returns the residue of division
print(8 % 3)

#* to update variable's value, use: <operator>=

number = 2
number += 2
print (number)

#* f-strings providees a way to print all variables and strings together
#* without type conversion
print(f"the number is: {number}")


